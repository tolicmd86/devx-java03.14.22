package com.java.class03;

public class ModuloOperator {
    public static void main(String[] args) {
      //% it's a reminder that to reach the number like 15%4 you need 3, from 12to15

        System.out.println(15%4);
        System.out.println(20%3);
        System.out.println(51%5);

        System.out.println(7%10);// the 7 it's the reminder because you can divide 7to 10

        System.out.println(12%2);//0-when it's perfectly dividing the results are 0
        System.out.println(51%2);//1 uneven numbers
        System.out.println(14%2);//0- whenever we have an even  number
        System.out.println(17%2);//1

        System.out.println(12%10);//2
        System.out.println(51%10);//1
        System.out.println(14%10);//4
        System.out.println(17%10);//7
        System.out.println(7%1);

        //  /10=7
        //  %10=8
        // the answer it's 78





    }
}
