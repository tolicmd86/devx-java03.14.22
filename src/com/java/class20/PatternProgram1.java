package com.java.class20;

public class PatternProgram1 {
    public static void main(String[] args) {

//print
        for (int i=5;i>=1;i--){
            int k=9;
            for(int j=1;j<=i;j++){
                System.out.print(k);
                k--;
            }
            System.out.println();
        }



    }
}
